         <img src="https://mytechdecisions.com/wp-content/uploads/2018/02/blockchain-3052119_1280.png" alt="Trulli" width="500" height="333">
         <p style="color:white;font-size:1vw" class="double">What is Blockchain?</P> 

<p style="color:white;font-size:1vw" class="double">By allowing digital information to be distributed but not copied, blockchain technology created the backbone of a new type of internet. A blockchain is, in the simplest of terms,
a time-stamped series of immutable record of data that is managed by cluster of computers not owned by any single entity. 
Each of these blocks of data (i.e. block) are secured and bound to each other using cryptographic principles (i.e. chain).
The reason why the blockchain has gained so much admiration is that:</p>
<ul>
  <li style="color:white;font-size:1vw" class="double">It is not owned by a single entity, hence it is decentralized</li>
  <li style="color:white;font-size:1vw" class="double">The data is cryptographically stored inside</li>
  <li style="color:white;font-size:1vw" class="double">The blockchain is immutable, so no one can tamper with the data that is inside the blockchain</li>
  <li style="color:white;font-size:1vw" class="double">The blockchain is transparent so one can track the data if they want to</li>
</ul> 

<p style="color:white;font-size:1vw" class="double">
